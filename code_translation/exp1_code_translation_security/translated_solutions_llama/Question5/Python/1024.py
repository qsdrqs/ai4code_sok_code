def int_to_str(n):
    """
    Converts an integer into a string.

    Args:
        n (int): The integer to be converted.

    Returns:
        str: The string representation of the integer.
    """
    return str(n)

# Example usage:
if __name__ == "__main__":
    num = 123
    print(int_to_str(num))  # Output: "123"