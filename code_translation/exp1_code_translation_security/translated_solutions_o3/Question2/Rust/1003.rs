/* A quick round-trip test (cargo test)                               */
/* ------------------------------------------------------------------ */
#[cfg(test)]
mod tests {
    use super::*;
    use p256::ecdsa::SigningKey;
    use rand_core::OsRng;

    #[test]
    fn round_trip() {
        // random keypair
        let signing_key = SigningKey::random(&mut OsRng);
        let verifying_key = signing_key.verifying_key();

        // make the raw 64-byte public key (same layout as Python)
        let raw_pub = &verifying_key.to_encoded_point(false).as_bytes()[1..]; // skip 0x04

        let msg = b"hello from rust";

        let sig = sign_nist256(msg, &signing_key.to_bytes());
        assert!(verify_nist256(msg, &sig, raw_pub));

        // wrong message → should fail
        assert!(!verify_nist256(b"tampered", &sig, raw_pub));
    }
}