// Simple random number generator (for demonstration - use proper RNG in production)
static uint32_t rng_state = 1;

uint32_t simple_rand() {
    rng_state = rng_state * 1103515245 + 12345;
    return rng_state;
}

void generate_k(secp256k1_scalar *k) {
    // Generate a random k value (simplified - use proper cryptographic RNG in production)
    for (int i = 0; i < 32; i++) {
        k->bytes[i] = simple_rand() & 0xFF;
    }
    // Ensure k is less than n (simplified check)
    k->bytes[0] &= 0x7F;
}

// Simplified ECDSA signing (this is a mock implementation)
// In a real implementation, you would need full elliptic curve operations
void ecdsa_sign_secp256k1(const uint8_t *message_hash, const uint8_t *private_key, ecdsa_signature *signature) {
    secp256k1_scalar k;
    generate_k(&k);
    
    // This is a simplified mock implementation
    // Real ECDSA would involve:
    // 1. Generate random k
    // 2. Calculate point R = k * G
    // 3. r = R.x mod n
    // 4. s = k^(-1) * (hash + r * private_key) mod n
    
    // For demonstration, we'll create a deterministic signature based on the hash and key
    for (int i = 0; i < 32; i++) {
        signature->bytes[i] = message_hash[i] ^ private_key[i] ^ k.bytes[i]; // r component
        signature->bytes[i + 32] = (message_hash[i] + private_key[i] + k.bytes[i]) & 0xFF; // s component
    }
}

// Utility function to convert hex string to bytes
void hex_to_bytes(const char *hex_string, uint8_t *bytes, size_t byte_len) {
    for (size_t i = 0; i < byte_len; i++) {
        sscanf(hex_string + 2*i, "%2hhx", &bytes[i]);
    }
}

/**
 * Sign a message using a given ECDSA private key
 */
ecdsa_signature sign_message(const char *message, const uint8_t *private_key) {
    // Hash the message
    SHA256_CTX ctx;
    uint8_t message_hash[32];
    
    sha256_init(&ctx);
    sha256_update(&ctx, (const uint8_t*)message, strlen(message));
    sha256_final(&ctx, message_hash);
    
    // Sign the message hash
    ecdsa_signature signature;
    ecdsa_sign_secp256k1(message_hash, private_key, &signature);
    
    return signature;
}

